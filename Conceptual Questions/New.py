#  Dictonaries Basic Functions

d = {"Alok Tiwari":"Sunil Tiwari", "Parth Tiwari":"Anil Tiwari","Sunny Tiwari":"Anil Tiwari","Divyansh Tiwari":"Om jee Tiwair"} # Here we making a Dictionarys

# Iterating over a Dictionary
for key in d:
    print(key,d[key])

print(d.keys())# It gives a list
print(d.values())# It gives a list
print(d.items()) # It gives a list which have inside a tuples